public class CharArray 
{

	public static void main(String[] args) 
	{
		char[] initials = { 'B', 'R', 'I', 'A', 'N' };
		
		for (int i = 0; i < initials.length; i++)
		{
			System.out.print(initials[i]);
		}	
	}
}
